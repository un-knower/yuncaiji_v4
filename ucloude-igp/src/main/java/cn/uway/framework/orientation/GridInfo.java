package cn.uway.framework.orientation;

/**
 * 栅格信息 GridInfo
 * 
 * @author chenrongqiang 2012-12-7
 */
public class GridInfo {

	private int gridM;

	private int gridN;

	public int getGridM() {
		return gridM;
	}

	public void setGridM(int gridM) {
		this.gridM = gridM;
	}

	public int getGridN() {
		return gridN;
	}

	public void setGridN(int gridN) {
		this.gridN = gridN;
	}

}
